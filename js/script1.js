function Branco(){
    document.getElementById('cor').style.backgroundColor = "#ffffff";
    document.getElementById('black').style.display = 'none';
    document.getElementById('white').style.display = 'inline';

}

function Preto(){
    document.getElementById('cor').style.backgroundColor = '#000';
    document.getElementById('white').style.display = 'none';
    document.getElementById('black').style.display = 'inline';

}